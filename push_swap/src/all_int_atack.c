#include "push_swap.h"

int		all_int_atack(t_stek **a)
{
	t_stek	*tmp_s;
	int		l;

	l = 0;
	tmp_s = *a;
	while (tmp_s)
	{
		tmp_s = tmp_s->next;
		l++;
	}
	return (l);
}
