#include "push_swap.h"

int			num_all_b(t_stek **b)
{
	t_stek	*tmp_s;
	int		l;

	l = 0;
	tmp_s = *b;
	while (tmp_s)
	{
		tmp_s = tmp_s->next;
		l++;
	}
	return (l);
}
