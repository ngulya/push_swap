#include "push_swap.h"

void		sa(t_stek **list)
{
	int		t;

	if ((*list) && (*list)->next)
	{
		t = (*list)->dig;
		(*list)->dig = (*list)->next->dig;
		(*list)->next->dig = t;
	}
}
