#include "push_swap.h"

t_stek		*pa(t_stek **a, t_stek **b)
{
	t_stek	*tmp;

	if (*b && *a)
	{
		tmp = *b;
		*b = (*b)->next;
		tmp->next = *a;
		return (tmp);
	}
	if (*b && !(*a))
	{
		tmp = *b;
		*b = (*b)->next;
		tmp->next = NULL;
		return (tmp);
	}
	return (*a);
}
