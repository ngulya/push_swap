#include "push_swap.h"

void		rra(t_stek **a)
{
	t_stek	*tmp;
	int		i;

	i = 0;
	tmp = *a;
	while (tmp)
	{
		i++;
		tmp = tmp->next;
	}
	while (--i > 0)
		ra(a);
}
